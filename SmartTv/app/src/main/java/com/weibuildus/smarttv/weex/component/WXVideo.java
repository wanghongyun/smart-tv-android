import java.util.Map;
import java.util.Timer;
import java.util.TimerTask;

@Component(lazyload = false)

/**
 * 播放器组件修改版
 */
public class WXVideo extends WXComponent<FrameLayout> {

    //是否自动播放
    private boolean mAutoPlay;
    //播放器控制
    private WXVideoView.Wrapper mWrapper;
    //播放器内核对象
    private MediaPlayer mMediaPlayer;
    //进度检测定时器
    private Timer mTimer = null;

    //是否已准备就绪
    private boolean mPrepared;
    //是否错误
    private boolean mError;
    //是否停止
    private boolean mStopped;

    @Deprecated
    public WXVideo(WXSDKInstance instance, WXDomObject dom, WXVContainer parent, String instanceId, boolean isLazy) {
        this(instance, dom, parent, isLazy);
    }

    public WXVideo(WXSDKInstance instance, WXDomObject dom, WXVContainer parent, boolean isLazy) {
        super(instance, dom, parent, isLazy);
    }

    /**
     * 初始化创建播放器
     *
     * @param context
     * @return
     */
    @Override
    protected FrameLayout initComponentHostView(@NonNull Context context) {

        final WXVideoView.Wrapper video = mWrapper = new WXVideoView.Wrapper(context);

        //错误监听
        video.setOnErrorListener(new MediaPlayer.OnErrorListener() {
            @Override
            public boolean onError(MediaPlayer mp, int what, int extra) {
                //关闭等待圈
                video.getProgressBar().setVisibility(View.GONE);
                //修改状态
                mPrepared = false;
                mError = true;
                // TODO: 错误回调
                onVideoPlayState(Constants.Event.ERROR);
                if (getDomObject().getEvents().contains(Constants.Event.FAIL)) {
                    WXVideo.this.notify(Constants.Event.FAIL, Constants.Value.STOP);
                }
                return true;
            }
        });
        //准备就绪监听
        video.setOnPreparedListener(new MediaPlayer.OnPreparedListener() {
            @Override
            public void onPrepared(MediaPlayer mediaPlayer) {
                //记录播放器内核对象
                mMediaPlayer = mediaPlayer;
                //关闭等待圈
                video.getProgressBar().setVisibility(View.GONE);
                //修改状态
                mPrepared = true;
                //自动播放
                if (mAutoPlay) {
                    video.start();
                }

                //前调5毫秒
                video.getVideoView().seekTo(5);
                //默认控制器
                if (video.getMediaController() != null) {
                    if (!mStopped) {
                        video.getMediaController().show(3);
                    } else {
                        video.getMediaController().hide();
                    }
                }

                //监听加载进度
                mediaPlayer.setOnBufferingUpdateListener(new MediaPlayer.OnBufferingUpdateListener() {
                    @Override
                    public void onBufferingUpdate(MediaPlayer mp, int percent) {
                        // TODO: 加载进度回调
                        onVideoLoading(mp.getDuration(), (int) (mp.getDuration() * (percent / 100.00f)));
                    }
                });
                mStopped = false;
            }
        });
        //监听播放完成
        video.setOnCompletionListener(new MediaPlayer.OnCompletionListener() {
            @Override
            public void onCompletion(MediaPlayer mediaPlayer) {
                // TODO: 播放完成回调
                onVideoPlayState(Constants.Event.FINISH);
                if (getDomObject().getEvents().contains(Constants.Event.FINISH)) {
                    WXVideo.this.notify(Constants.Event.FINISH, Constants.Value.STOP);
                }
                // TODO: 播放进度回调
                onVideoProgressing(mMediaPlayer.getDuration(), mMediaPlayer.getCurrentPosition());
            }
        });
        //监听播放暂停
        video.setOnVideoPauseListener(new WXVideoView.VideoPlayListener() {

            @Override
            public void onPause() {
                // TODO: 暂停状态回调
                onVideoPlayState(Constants.Event.PAUSE);
                if (getDomObject().getEvents().contains(Constants.Value.PAUSE)) {
                    WXVideo.this.notify(Constants.Event.PAUSE, Constants.Value.PAUSE);
                }
            }

            @Override
            public void onStart() {
                // TODO: 播放状态回调
                onVideoPlayState(Constants.Value.PLAY);
                if (getDomObject().getEvents().contains(Constants.Event.START)) {
                    WXVideo.this.notify(Constants.Event.START, Constants.Value.PLAY);
                }
            }
        });
        return video;
    }

    //#########默认方法部分
    private void notify(String event, String newStatus) {
        Map<String, Object> params = new HashMap<>(2);
        params.put(Constants.Name.PLAY_STATUS, newStatus);
        params.put("timeStamp", System.currentTimeMillis());

        Map<String, Object> domChanges = new HashMap<>();
        Map<String, Object> attrsChanges = new HashMap<>();
        attrsChanges.put(Constants.Name.PLAY_STATUS, newStatus);
        domChanges.put("attrs", attrsChanges);

        WXSDKManager.getInstance().fireEvent(getInstanceId(), getRef(), event, params, domChanges);
    }

    @Override
    public void bindData(WXComponent component) {
        super.bindData(component);
        addEvent(Constants.Event.APPEAR);
    }

    @Override
    public void notifyAppearStateChange(String wxEventType, String direction) {
        super.notifyAppearStateChange(wxEventType, direction);
        mWrapper.createVideoViewIfVisible();
    }

    @Override
    public void destroy() {
        if (mTimer != null) {
            mTimer.cancel();
            mTimer = null;
        }
        super.destroy();
    }

    @Override
    protected boolean setProperty(String key, Object param) {
        switch (key) {
            case Constants.Name.SRC:
                String src = WXUtils.getString(param, null);
                if (src != null) {
                    setSrc(src);
                }
                return true;
            case Constants.Name.AUTO_PLAY:
                Boolean result = WXUtils.getBoolean(param, null);
                if (result != null) {
                    setAutoPlay(result);
                }
                return true;
            case Constants.Name.PLAY_STATUS:
                String status = WXUtils.getString(param, null);
                if (status != null) {
                    setPlaystatus(status);
                }
                return true;
        }
        return super.setProperty(key, param);
    }

    //地址
    @WXComponentProp(name = Constants.Name.SRC)
    public void setSrc(String src) {
        if (TextUtils.isEmpty(src) || getHostView() == null) {
            return;
        }

        if (!TextUtils.isEmpty(src)) {
            mWrapper.setVideoURI(Uri.parse(src));
            mWrapper.getProgressBar().setVisibility(View.VISIBLE);
        }
    }

    //自动播放
    @WXComponentProp(name = "autoplay")
    public void setAutoPlay(boolean autoPlay) {
        mAutoPlay = autoPlay;
        if (autoPlay) {
            mWrapper.createIfNotExist();
            mWrapper.start();
        }
    }

    /**
     * 播放状态
     *
     * @param playstatus
     */
    @WXComponentProp(name = "playstate")
    public void setPlaystatus(String playstatus) {
        if (mPrepared && !mError && !mStopped) {
            if (playstatus.equals(Constants.Value.PLAY)) {
                mWrapper.start();
            } else if (playstatus.equals(Constants.Value.PAUSE)) {
                mWrapper.pause();
            } else if (playstatus.equals(Constants.Value.STOP)) {
                mWrapper.stopPlayback();
                mStopped = true;
                // TODO: 停止状态回调
                onVideoPlayState(Constants.Value.STOP);
                if (getDomObject().getEvents().contains(Constants.Value.STOP)) {
                    WXVideo.this.notify(Constants.Value.STOP, Constants.Value.STOP);
                }
            }
        } else if ((mError || mStopped) && playstatus.equals(Constants.Value.PLAY)) {
            mError = false;
            mWrapper.resume();
            mWrapper.getProgressBar().setVisibility(View.VISIBLE);
        }
    }

    /**
     * 设置进度
     *
     * @param seekTo
     */
    @WXComponentProp(name = "seekto")
    public void seekTo(double seekTo) {
        if (seekTo <= 0) {
            if(mWrapper.getVideoView().getCurrentPosition()>5){
                mWrapper.seekTo(5);
            }
            return;
        }
        if (seekTo >= 1) {
            if(mWrapper.getVideoView().getCurrentPosition()<mWrapper.getVideoView().getDuration()-5){
                mWrapper.seekTo(mWrapper.getVideoView().getDuration()-5);
            }
            return;
        }
        int msec = (int) (mWrapper.getVideoView().getDuration() * seekTo);
        //时差大于一秒
        if(Math.abs(msec - mWrapper.getVideoView().getCurrentPosition())>1000){
            mWrapper.seekTo(msec);
        }
    }

    /**
     * 横竖屏切换 landscape 横屏（全屏）  portrait 竖屏（非全屏）none 置空不会有任何操作
     *
     * @param screen
     */
    @WXComponentProp(name = "fullscreen")
    public void fullScreen(String screen) {
        if (getContext() instanceof Activity) {
            Activity act = (Activity) getContext();
            if ("landscape".equals(screen)) {
                if (act.getRequestedOrientation() != ActivityInfo.SCREEN_ORIENTATION_SENSOR_LANDSCAPE) {
                    act.setRequestedOrientation(ActivityInfo.SCREEN_ORIENTATION_SENSOR_LANDSCAPE);
                }
            }
            if ("portrait".equals(screen)) {
                if (act.getRequestedOrientation() == ActivityInfo.SCREEN_ORIENTATION_SENSOR_LANDSCAPE) {
                    act.setRequestedOrientation(ActivityInfo.SCREEN_ORIENTATION_PORTRAIT);
                }
            }
        }
    }

    //#############播放进度监听线程代码################

    /**
     * 自定义定时任务
     */
    class MyTimerTask extends TimerTask {
        @Override
        public void run() {
            if (getContext() instanceof Activity) {
                //转到UI线程
                ((Activity) getContext()).runOnUiThread(new Runnable() {
                    @Override
                    public void run() {
                        runTimerTask();
                    }
                });
            }
        }
    }

    /**
     * 运行进度查询操作，并回调接口
     */
    private void runTimerTask() {
        try {
            if (mMediaPlayer == null)
                return;
            if (mMediaPlayer.isPlaying()) {
                // 获取当前播放进度，和文件总长，并调用 播放进度接口方法。
                // TODO: 播放进度回调
                onVideoProgressing(mMediaPlayer.getDuration(), mMediaPlayer.getCurrentPosition());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    //#############播放器状态监听接口回调################


    public void onVideoPlayState(String state) {
        Map<String, Object> params = new HashMap<>();
        params.put("state", state);
        WXSDKManager.getInstance().fireEvent(mInstanceId, getRef(), "playstate", params);

        //播放进度监听定时器开关
        if (TextUtils.equals(Constants.Value.PLAY, state)) {
            //播放
            try {
                if (mTimer != null) {
                    mTimer.cancel();
                }
                mTimer = new Timer();
                mTimer.schedule(new MyTimerTask(), 100, 100);
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            //其他原因播放停止
            if (mTimer != null) {
                mTimer.cancel();
                mTimer = null;
            }
        }
    }

    public void onVideoLoading(int duration, int buffer_position) {
        Map<String, Object> params = new HashMap<>();
        params.put("duration", duration);
        params.put("load", buffer_position);
        WXSDKManager.getInstance().fireEvent(mInstanceId, getRef(), "loading", params);
    }

    public void onVideoProgressing(int duration, int progress) {
        Map<String, Object> params = new HashMap<>();
        params.put("duration", duration);
        params.put("progress", progress);
        WXSDKManager.getInstance().fireEvent(mInstanceId, getRef(), "progressing", params);
    }
}
